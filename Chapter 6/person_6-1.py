person_0 = {'first_name': 'pam', 'last_name': 'beesley', "age": 26, 'city': 'scranton'}

print(f"This person's first name is {person_0['first_name'].title()}.")
print(f"This person's last name is {person_0['last_name'].title()}.")
print(f"Their age is {person_0['age']}.")
print(f"And they live in the city of {person_0['city'].title()}")