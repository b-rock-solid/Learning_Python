# You can perform other mathematical comparisons

# Set age to 19
age = 19

# Check age is less than 21
if age < 21:
    print('Your age is less than 21')

# Check age is less than or equal to 21
if age <= 21:
    print('Your age is less than or equal to 21')

# Check age is greater than 21
if age > 21:
    print('You are old enough! You may enter!')
else:
    print('Access denied. Not older than 21!')

# Check age is greater than or equal to 21
if age >= 21:
    print('You are 21 or older. You may enter!')
else:
    print('You do not meet the required age of 21. Access denied!')