# Choose a colour for an alien as you did in Exercise 5-3, and write an if-else chain.
# If alien colour is green, print a message that the player earned 5 points
# If alien colour isn't green, print a statement that the player just earned 10 points
# Write a version that runs the 'if' block and another version that runs the 'else' block
########################################################################################################################

# If block being run
alien_colour = 'green'

if alien_colour == 'green':
    print("5 Points earned! Keep it up!")
else:
    print("10 Points earned! Nice shot!")

# Else block being run
alien_colour = 'red'

if alien_colour == 'green':
    print("5 Points earned! Keep it up!")
else:
    print("10 Points earned! Nice shot!")