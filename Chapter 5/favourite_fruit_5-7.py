favourite_fruits = ['apple', 'orange', 'mango']

for fruit in favourite_fruits:
    if fruit == 'apple':
        print(f"You really like {fruit.title()}!")
    if fruit == 'banana':
        print(f"You really like {fruit.title()}!")
    if fruit == 'peach':
        print(f"You really like {fruit.title()}!")
    if fruit == 'mango':
        print(f"You really like {fruit.title()}!")
    if fruit == 'plum':
        print(f"You really like {fruit.title()}!")
